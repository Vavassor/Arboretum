GraphemeClusterBreak get_grapheme_cluster_break(char32_t c)
{
    const u32 BLOCK_SIZE = 256;
    ASSERT(c < 0x110000);
    u32 block_offset = grapheme_cluster_break_stage1[c / BLOCK_SIZE] * BLOCK_SIZE;
    u8 value = grapheme_cluster_break_stage2[block_offset + c % BLOCK_SIZE];

    ASSERT(value >= 0 && value <= 17);
    return static_cast<GraphemeClusterBreak>(value);
}

struct Basket
{
    Stack* stack;
    const char* text;
    GraphemeClusterBreak* breaks;
    int count;
    int cap;
    int breaks_base;
    int text_base;
    int text_size;
};

static bool get_break_at(Basket* basket, int start_index, GraphemeClusterBreak* result)
{
    int breaks_index = start_index - basket->text_base;
    int breaks_furthest = basket->breaks_base + basket->count;

    if(breaks_index >= basket->breaks_base && breaks_index <= breaks_furthest)
    {
        *result = basket->breaks[breaks_index];
        return true;
    }

    char32_t codepoint;
    int index = utf8_get_prior_codepoint(basket->text, start_index, &codepoint);
    if(index < 0)
    {
        return false;
    }

    if(basket->count + 1 >= basket->cap)
    {
        int cap = 2 * basket->cap;
        basket->breaks = STACK_REALLOCATE(basket->stack, GraphemeClusterBreak, basket->breaks, cap);
        basket->cap = cap;
    }

    GraphemeClusterBreak b = get_grapheme_cluster_break(codepoint);
    basket->breaks[basket->count] = b;
    basket->count += 1;

    *result = b;
    return true;
}

bool allow_grapheme_cluster_break(Basket* basket, int index)
{
    // Always break at the beginning and end of text.
    if(index == 0 || index >= basket->text_size)
    {
        return true;
    }

    GraphemeClusterBreak a;
    GraphemeClusterBreak b;
    bool got_a = get_break_at(basket, index - 1, &a);
    bool got_b = get_break_at(basket, index, &b);
    if(!got_a || !got_b)
    {
        return true;
    }

    // Do not break between a carriage return and line feed.
    bool left = a == GraphemeClusterBreak::Carriage_Return;
    bool right = b == GraphemeClusterBreak::Line_Feed;
    if(left && right)
    {
        return false;
    }

    // Break between control characters, line feeds, and carriage returns that
    // don't violate the prior carriage return/line feed rule.
    left = a == GraphemeClusterBreak::Carriage_Return
            || a == GraphemeClusterBreak::Line_Feed
            || a == GraphemeClusterBreak::Control;
    right = b == GraphemeClusterBreak::Carriage_Return
        || b == GraphemeClusterBreak::Line_Feed
        || b == GraphemeClusterBreak::Control;
    if(left || right)
    {
        return true;
    }

    // Do not break Hangul syllable sequences.
    left = a == GraphemeClusterBreak::Hangul_Syllable_L;
    right = b == GraphemeClusterBreak::Hangul_Syllable_L
        || b == GraphemeClusterBreak::Hangul_Syllable_V
        || b == GraphemeClusterBreak::Hangul_Syllable_LV
        || b == GraphemeClusterBreak::Hangul_Syllable_LVT;
    if(left && right)
    {
        return false;
    }

    left = a == GraphemeClusterBreak::Hangul_Syllable_V
        || a == GraphemeClusterBreak::Hangul_Syllable_LV;
    right = b == GraphemeClusterBreak::Hangul_Syllable_V
        || b == GraphemeClusterBreak::Hangul_Syllable_T;
    if(left && right)
    {
        return false;
    }

    left = a == GraphemeClusterBreak::Hangul_Syllable_T
        || a == GraphemeClusterBreak::Hangul_Syllable_LVT;
    right = b == GraphemeClusterBreak::Hangul_Syllable_T;
    if(left && right)
    {
        return false;
    }

    // Do not break before extending characters, zero-width joiner, or spacing
    // marks.
    right = b == GraphemeClusterBreak::Extend
        || b == GraphemeClusterBreak::Zero_Width_Joiner
        || b == GraphemeClusterBreak::Spacing_Mark;
    if(right)
    {
        return false;
    }

    // Do not break after Prepend characters.
    left = a == GraphemeClusterBreak::Prepend;
    if(left)
    {
        return false;
    }

    // Do not break within emoji modifier sequences.
    left = a == GraphemeClusterBreak::Extend;
    right = b == GraphemeClusterBreak::Emoji_Modifier;
    if(left && right)
    {
        return false;
    }

    // Do not break within emoji zero-width joiner sequences.
    left = a == GraphemeClusterBreak::Zero_Width_Joiner;
    right = b == GraphemeClusterBreak::Glue_After_ZWJ
        || b == GraphemeClusterBreak::Emoji_Base_GAZ;
    if(left && right)
    {
        return false;
    }

    // Do not break between regional indicator (RI) symbols if there is an odd
    // number of RI characters before the break point.
    left = a == GraphemeClusterBreak::Regional_Indicator;
    right = b == GraphemeClusterBreak::Regional_Indicator;
    if(left && right)
    {
        int count = 1;
        for(int i = index - 2; i >= 0; i -= 1)
        {
            GraphemeClusterBreak value;
            bool got = get_break_at(basket, i, &value);
            if(!got || value != GraphemeClusterBreak::Regional_Indicator)
            {
                break;
            }
            count += 1;
        }
        if(count & 1)
        {
            return false;
        }
    }

    return true;
}

int find_prior_beginning_of_grapheme_cluster(const char* text, int start_index, Stack* stack)
{
    Basket basket = {};
    basket.stack = stack;
    basket.text = text;
    basket.cap = 32;
    basket.breaks = STACK_ALLOCATE(stack, GraphemeClusterBreak, basket.cap);
    basket.text_size = string_size(text);
    basket.text_base = start_index;

    while(basket.text_base >= 0)
    {
        bool allowed = allow_grapheme_cluster_break(&basket, basket.text_base);
        basket.breaks_base += 1;
        basket.text_base -= 1;
        if(allowed)
        {
            break;
        }
    }

    STACK_DEALLOCATE(stack, basket.breaks);

    return basket.text_base;
}

int find_next_end_of_grapheme_cluster(const char* text, int start_index, Stack* stack)
{
    Basket basket = {};
    basket.stack = stack;
    basket.text = text;
    basket.cap = 32;
    basket.breaks = STACK_ALLOCATE(stack, GraphemeClusterBreak, basket.cap);
    basket.text_size = string_size(text);
    basket.text_base = MIN(start_index + 1, basket.text_size);

    while(basket.text_base < basket.text_size)
    {
        bool allowed = allow_grapheme_cluster_break(&basket, basket.text_base);
        if(allowed)
        {
            break;
        }
        basket.breaks_base += 1;
        basket.text_base += 1;
    }

    STACK_DEALLOCATE(stack, basket.breaks);

    return basket.text_base;
}
